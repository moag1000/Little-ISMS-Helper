<?php

declare(strict_types=1);

namespace App\Tests\Service\Library;

use App\Command\LoadBsiItGrundschutzCatalogueCommand;
use PHPUnit\Framework\Attributes\DataProvider;
use PHPUnit\Framework\Attributes\Test;
use PHPUnit\Framework\TestCase;
use Symfony\Component\Yaml\Yaml;

/**
 * Unit tests for the official BSI Kreuzreferenztabelle ISO/IEC 27001:2022
 * to IT-Grundschutz mapping YAML.
 *
 * Source: BSI "Zuordnung ISO/IEC 27001 zum IT-Grundschutz" (6. Edition 2023),
 * https://www.bsi.bund.de/SharedDocs/Downloads/DE/BSI/Grundschutz/IT-GS-Kompendium/
 * Zuordnung_ISO_und_IT_Grundschutz_Edit_6.pdf?__blob=publicationFile&v=2
 *
 * Integrity guard: this mapping is amtlich (official BSI publication).
 * Pairs must NOT be generated by AI inference; they must be verbatim from BSI.
 */
final class IsoToBsiGrundschutzOfficialCrtTest extends TestCase
{
    private const MAPPING_FILE = __DIR__ . '/../../../fixtures/library/mappings/iso27001-2022_to_bsi-grundschutz_official-crt_v1.yaml';

    /** @var array<mixed> */
    private static array $data = [];

    public static function setUpBeforeClass(): void
    {
        self::$data = Yaml::parseFile(self::MAPPING_FILE);
    }

    #[Test]
    public function yaml_file_exists_and_is_readable(): void
    {
        self::assertFileExists(self::MAPPING_FILE);
        self::assertFileIsReadable(self::MAPPING_FILE);
    }

    #[Test]
    public function top_level_schema_keys_present(): void
    {
        self::assertArrayHasKey('schema_version', self::$data);
        self::assertArrayHasKey('library', self::$data);
        self::assertArrayHasKey('mappings', self::$data);
    }

    #[Test]
    public function provenance_source_is_official_bsi_crosswalk(): void
    {
        $provenance = self::$data['library']['provenance'] ?? [];
        self::assertSame(
            'official_bsi_crosswalk',
            $provenance['primary_source'] ?? null,
            'provenanceSource must be "official_bsi_crosswalk" — this data is the amtlich BSI publication, not AI inference',
        );
    }

    #[Test]
    public function provenance_url_points_to_bsi_bund_de(): void
    {
        $url = self::$data['library']['provenance']['primary_source_url'] ?? '';
        self::assertStringContainsString(
            'bsi.bund.de',
            $url,
            'primary_source_url must reference bsi.bund.de (official BSI download)',
        );
        self::assertStringContainsString(
            'Zuordnung_ISO_und_IT_Grundschutz',
            $url,
            'URL must reference the Zuordnung document, not a generic page',
        );
    }

    #[Test]
    public function methodology_type_is_official_published_crosswalk(): void
    {
        $type = self::$data['library']['methodology']['type'] ?? null;
        self::assertSame('official_published_crosswalk', $type,
            'methodology.type must be official_published_crosswalk for amtlich data');
    }

    #[Test]
    public function mapping_covers_all_iso27001_annex_a_controls(): void
    {
        $sources = array_column(self::$data['mappings'] ?? [], 'source');

        // All 93 Annex A controls (A.5.1-A.5.37, A.6.1-A.6.8, A.7.1-A.7.14, A.8.1-A.8.34)
        $expectedAnnexA = array_merge(
            array_map(fn(int $n): string => "A.5.{$n}", range(1, 37)),
            array_map(fn(int $n): string => "A.6.{$n}", range(1, 8)),
            array_map(fn(int $n): string => "A.7.{$n}", range(1, 14)),
            array_map(fn(int $n): string => "A.8.{$n}", range(1, 34)),
        );

        foreach ($expectedAnnexA as $control) {
            self::assertContains(
                $control,
                $sources,
                "Missing Annex A control in official BSI crosswalk: {$control}",
            );
        }
    }

    #[Test]
    public function mapping_covers_iso27001_normative_clauses_4_to_10(): void
    {
        $sources = array_column(self::$data['mappings'] ?? [], 'source');

        $clauses = ['4.1', '4.2', '4.3', '4.4', '5.1', '5.2', '5.3',
            '6.1.1', '6.1.2', '6.1.3', '6.2',
            '7.1', '7.2', '7.3', '7.4', '7.5.1', '7.5.2', '7.5.3',
            '8.1', '8.2', '8.3',
            '9.1', '9.2', '9.3',
            '10.1', '10.2'];

        foreach ($clauses as $clause) {
            self::assertContains(
                $clause,
                $sources,
                "Missing normative clause in official BSI crosswalk: {$clause}",
            );
        }
    }

    #[Test]
    public function all_pairs_have_required_fields(): void
    {
        foreach (self::$data['mappings'] ?? [] as $i => $entry) {
            self::assertArrayHasKey('source', $entry, "Pair #{$i} missing source");
            self::assertArrayHasKey('target', $entry, "Pair #{$i} missing target");
            self::assertArrayHasKey('relationship', $entry, "Pair #{$i} missing relationship");
            self::assertArrayHasKey('confidence', $entry, "Pair #{$i} missing confidence");
            self::assertArrayHasKey('rationale', $entry, "Pair #{$i} missing rationale");
        }
    }

    #[Test]
    public function all_targets_reference_valid_bsi_grundschutz_identifiers(): void
    {
        $validPrefixes = [
            'ISMS.', 'ORP.', 'CON.', 'OPS.', 'DER.', 'APP.',
            'SYS.', 'IND.', 'NET.', 'INF.', 'SYS.', 'DER.',
        ];

        foreach (self::$data['mappings'] ?? [] as $entry) {
            $target = $entry['target'];
            $matched = false;
            foreach ($validPrefixes as $prefix) {
                if (str_starts_with($target, $prefix)) {
                    $matched = true;
                    break;
                }
            }
            self::assertTrue(
                $matched,
                "Target '{$target}' does not match a known IT-Grundschutz Baustein prefix",
            );
        }
    }

    #[Test]
    public function all_relationships_are_valid_enum_values(): void
    {
        $validRelationships = ['equivalent', 'subset', 'superset', 'partial_overlap', 'related'];
        foreach (self::$data['mappings'] ?? [] as $entry) {
            self::assertContains(
                $entry['relationship'],
                $validRelationships,
                "Invalid relationship value: {$entry['relationship']} for source {$entry['source']}",
            );
        }
    }

    #[Test]
    public function all_confidence_values_are_valid(): void
    {
        $validConfidence = ['high', 'medium', 'low'];
        foreach (self::$data['mappings'] ?? [] as $entry) {
            self::assertContains(
                $entry['confidence'],
                $validConfidence,
                "Invalid confidence: {$entry['confidence']} for source {$entry['source']}",
            );
        }
    }

    #[Test]
    public function mapping_count_is_at_least_100_pairs(): void
    {
        self::assertGreaterThanOrEqual(
            100,
            count(self::$data['mappings'] ?? []),
            'Official BSI crosswalk must cover at least 100 pairs (93 Annex A + clauses 4-10)',
        );
    }

    #[Test]
    public function publisher_is_bsi(): void
    {
        $publisher = self::$data['library']['provenance']['publisher'] ?? '';
        self::assertStringContainsString(
            'BSI',
            $publisher,
            'Publisher must reference BSI (Bundesamt fuer Sicherheit in der Informationstechnik)',
        );
    }

    // ─── Framework-code alignment regression guards ───────────────────────────
    // These tests ensure the YAML framework codes match the DB codes set by
    // LoadBsiItGrundschutzCatalogueCommand::FRAMEWORK_CODE ('BSI_GRUNDSCHUTZ').
    // A mismatch causes MappingLibraryLoader to silently skip ALL mapping pairs
    // (exact-code match at line 64 / line 114 of MappingLibraryLoader.php).

    /**
     * The expected DB framework code for BSI IT-Grundschutz, as set by
     * LoadBsiItGrundschutzCatalogueCommand (private const FRAMEWORK_CODE).
     * Verified via reflection to stay in sync with the command.
     */
    private static function expectedBsiFrameworkCode(): string
    {
        $ref = new \ReflectionClass(LoadBsiItGrundschutzCatalogueCommand::class);

        return $ref->getConstant('FRAMEWORK_CODE'); // @phpstan-ignore-line (private const via reflection)
    }

    #[Test]
    public function official_crt_target_framework_matches_db_framework_code(): void
    {
        $expected = self::expectedBsiFrameworkCode();
        $actual = self::$data['library']['target_framework'] ?? null;
        self::assertSame(
            $expected,
            $actual,
            sprintf(
                'official-crt target_framework "%s" must match DB code "%s" from LoadBsiItGrundschutzCatalogueCommand::FRAMEWORK_CODE — '
                . 'a mismatch causes MappingLibraryLoader to silently skip all mapping pairs.',
                (string) $actual,
                $expected,
            ),
        );
    }

    #[Test]
    public function official_crt_source_framework_is_iso27001(): void
    {
        $actual = self::$data['library']['source_framework'] ?? null;
        self::assertSame(
            'ISO27001',
            $actual,
            sprintf(
                'official-crt source_framework "%s" must be "ISO27001" to match the ISO framework DB code.',
                (string) $actual,
            ),
        );
    }

    /**
     * @return array<string, array{string}>
     */
    public static function allIsoBsiMappingFiles(): array
    {
        $base = __DIR__ . '/../../../fixtures/library/mappings/';

        return [
            'official-crt' => [$base . 'iso27001-2022_to_bsi-grundschutz_official-crt_v1.yaml'],
            'v1.0' => [$base . 'iso27001-2022_to_bsi-it-grundschutz_v1.0.yaml'],
            'v2.0-2024' => [$base . 'iso27001-2022_to_bsi-grundschutz-2024_v2.0.yaml'],
        ];
    }

    #[Test]
    #[DataProvider('allIsoBsiMappingFiles')]
    public function iso_bsi_mapping_target_framework_matches_db_code(string $file): void
    {
        $data = Yaml::parseFile($file);
        $expected = self::expectedBsiFrameworkCode();
        $actual = $data['library']['target_framework'] ?? null;
        self::assertSame(
            $expected,
            $actual,
            sprintf(
                'File %s: target_framework "%s" must equal DB code "%s" (LoadBsiItGrundschutzCatalogueCommand::FRAMEWORK_CODE). '
                . 'Hyphen vs underscore mismatch silently breaks ALL ISO→BSI mappings.',
                basename($file),
                (string) $actual,
                $expected,
            ),
        );
    }

    #[Test]
    #[DataProvider('allIsoBsiMappingFiles')]
    public function iso_bsi_mapping_source_framework_is_iso27001(string $file): void
    {
        $data = Yaml::parseFile($file);
        $actual = $data['library']['source_framework'] ?? null;
        self::assertSame(
            'ISO27001',
            $actual,
            sprintf(
                'File %s: source_framework "%s" must be "ISO27001" to match the ISO framework DB code.',
                basename($file),
                (string) $actual,
            ),
        );
    }

    #[Test]
    public function reverse_bsi_to_iso_source_framework_matches_db_code(): void
    {
        $file = __DIR__ . '/../../../fixtures/library/mappings/bsi-it-grundschutz_to_iso27001-2022_v1.0.yaml';
        $data = Yaml::parseFile($file);
        $expected = self::expectedBsiFrameworkCode();
        $actual = $data['library']['source_framework'] ?? null;
        self::assertSame(
            $expected,
            $actual,
            sprintf(
                'Reverse mapping source_framework "%s" must equal DB code "%s". '
                . 'Mismatch prevents reverse-direction lookups.',
                (string) $actual,
                $expected,
            ),
        );
    }
}
