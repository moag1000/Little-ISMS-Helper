<?php

declare(strict_types=1);

namespace App\Tests\Service\Library;

use App\Command\LoadC52020FullCatalogueCommand;
use PHPUnit\Framework\Attributes\DataProvider;
use PHPUnit\Framework\Attributes\Test;
use PHPUnit\Framework\TestCase;
use Symfony\Component\Yaml\Yaml;

/**
 * Alignment tests for the official BSI C5:2020 ↔ ISO/IEC 27001:2022 crosswalk YAML.
 *
 * Source: BSI "Referenzierung des Kriterienkatalog Cloud Computing C5:2020 auf
 * internationale Standards — Kreuzreferenztabelle zu ISO/IEC 27001:2022"
 * https://www.bsi.bund.de/SharedDocs/Downloads/DE/BSI/CloudComputing/Anforderungskatalog/2020/C5_2022_Referenztabelle_ISO27001.xlsx
 * (BSI, Stand 2023-06-21)
 *
 * Integrity guard: this mapping is amtlich (official BSI publication).
 * Pairs MUST NOT be generated by AI inference; they must be verbatim from BSI.
 */
final class BsiC5ToIso27001OfficialCrtTest extends TestCase
{
    private const MAPPING_FILE = __DIR__ . '/../../../fixtures/library/mappings/bsi-c5-2020_to_iso27001-2022_official-crt_v1.yaml';

    /** @var array<mixed> */
    private static array $data = [];

    public static function setUpBeforeClass(): void
    {
        self::$data = Yaml::parseFile(self::MAPPING_FILE);
    }

    // ─── Structural integrity ────────────────────────────────────────────────

    #[Test]
    public function yaml_file_exists_and_is_readable(): void
    {
        self::assertFileExists(self::MAPPING_FILE);
        self::assertFileIsReadable(self::MAPPING_FILE);
    }

    #[Test]
    public function top_level_schema_keys_present(): void
    {
        self::assertArrayHasKey('schema_version', self::$data);
        self::assertArrayHasKey('library', self::$data);
        self::assertArrayHasKey('mappings', self::$data);
    }

    // ─── Provenance & integrity guards ───────────────────────────────────────

    #[Test]
    public function provenance_source_is_official_bsi_c5_iso_crosswalk(): void
    {
        $provenance = self::$data['library']['provenance'] ?? [];
        self::assertSame(
            'official_bsi_c5_iso_crosswalk',
            $provenance['primary_source'] ?? null,
            'primary_source must be "official_bsi_c5_iso_crosswalk" — this data is the amtlich BSI publication, not AI inference',
        );
    }

    #[Test]
    public function provenance_url_points_to_bsi_bund_de_c5_excel(): void
    {
        $url = self::$data['library']['provenance']['primary_source_url'] ?? '';
        self::assertStringContainsString(
            'bsi.bund.de',
            $url,
            'primary_source_url must reference bsi.bund.de (official BSI download)',
        );
        self::assertStringContainsString(
            'C5_2022_Referenztabelle_ISO27001',
            $url,
            'URL must reference the C5:2020-to-ISO27001:2022 reference table, not a generic page',
        );
    }

    #[Test]
    public function methodology_type_is_official_published_crosswalk(): void
    {
        $type = self::$data['library']['methodology']['type'] ?? null;
        self::assertSame(
            'official_published_crosswalk',
            $type,
            'methodology.type must be official_published_crosswalk for amtlich data',
        );
    }

    #[Test]
    public function publisher_is_bsi(): void
    {
        $publisher = self::$data['library']['provenance']['publisher'] ?? '';
        self::assertStringContainsString(
            'BSI',
            $publisher,
            'Publisher must reference BSI (Bundesamt fuer Sicherheit in der Informationstechnik)',
        );
    }

    // ─── Pair-level integrity ────────────────────────────────────────────────

    #[Test]
    public function all_pairs_have_required_fields(): void
    {
        foreach (self::$data['mappings'] ?? [] as $i => $entry) {
            self::assertArrayHasKey('source', $entry, "Pair #{$i} missing source");
            self::assertArrayHasKey('target', $entry, "Pair #{$i} missing target");
            self::assertArrayHasKey('relationship', $entry, "Pair #{$i} missing relationship");
            self::assertArrayHasKey('confidence', $entry, "Pair #{$i} missing confidence");
            self::assertArrayHasKey('bsi_sn', $entry, "Pair #{$i} missing bsi_sn (Sicherheitsniveau indicator)");
            self::assertArrayHasKey('rationale', $entry, "Pair #{$i} missing rationale");
        }
    }

    #[Test]
    public function all_sources_are_valid_c5_criterion_ids(): void
    {
        $validPrefixes = ['OIS-', 'SP-', 'HR-', 'AM-', 'PS-', 'OPS-', 'IDM-', 'CRY-', 'COS-', 'PI-', 'DEV-', 'SSO-', 'SIM-', 'BCM-', 'COM-', 'INQ-', 'PSS-'];

        foreach (self::$data['mappings'] ?? [] as $entry) {
            $source = $entry['source'];
            $matched = false;
            foreach ($validPrefixes as $prefix) {
                if (str_starts_with($source, $prefix)) {
                    $matched = true;
                    break;
                }
            }
            self::assertTrue(
                $matched,
                "Source '{$source}' does not match a known BSI C5 criterion prefix (OIS-, SP-, HR-, AM-, PS-, OPS-, IDM-, CRY-, COS-, PI-, DEV-, SSO-, SIM-, BCM-, COM-, INQ-, PSS-)",
            );
        }
    }

    #[Test]
    public function all_relationships_are_valid_enum_values(): void
    {
        $validRelationships = ['equivalent', 'superset', 'partial_overlap', 'related'];
        foreach (self::$data['mappings'] ?? [] as $entry) {
            self::assertContains(
                $entry['relationship'],
                $validRelationships,
                "Invalid relationship value: {$entry['relationship']} for source {$entry['source']}",
            );
        }
    }

    #[Test]
    public function all_confidence_values_are_valid(): void
    {
        $validConfidence = ['high', 'medium', 'low'];
        foreach (self::$data['mappings'] ?? [] as $entry) {
            self::assertContains(
                $entry['confidence'],
                $validConfidence,
                "Invalid confidence: {$entry['confidence']} for source {$entry['source']}",
            );
        }
    }

    #[Test]
    public function all_bsi_sn_values_are_valid(): void
    {
        $validSn = ['0', '+', '-'];
        foreach (self::$data['mappings'] ?? [] as $entry) {
            self::assertContains(
                $entry['bsi_sn'],
                $validSn,
                "Invalid bsi_sn: {$entry['bsi_sn']} for source {$entry['source']} — only 0/+/- are valid (n/a entries have no ISO target and are omitted)",
            );
        }
    }

    #[Test]
    public function mapping_count_is_at_least_200_pairs(): void
    {
        self::assertGreaterThanOrEqual(
            200,
            count(self::$data['mappings'] ?? []),
            'Official BSI C5:2020 to ISO 27001:2022 crosswalk must cover at least 200 pairs',
        );
    }

    #[Test]
    public function covers_key_c5_criteria(): void
    {
        $sources = array_column(self::$data['mappings'] ?? [], 'source');

        // Spot-check a selection of well-known C5 criteria that must be present
        $expectedCriteria = [
            'OIS-01', // ISMS
            'OIS-02', // Security policy
            'OIS-06', // Risk management
            'IDM-01', // Identity management
            'CRY-01', // Encryption policy
            'OPS-10', // Logging concept
            'SIM-01', // Security incident management
            'BCM-01', // BCM
            'COM-01', // Compliance
        ];

        foreach ($expectedCriteria as $criterion) {
            self::assertContains(
                $criterion,
                $sources,
                "Missing key C5 criterion in official crosswalk: {$criterion}",
            );
        }
    }

    // ─── Framework-code alignment regression guards ───────────────────────────
    // These tests ensure the YAML framework codes match the DB codes set by
    // LoadC52020FullCatalogueCommand::getFrameworkCode() ('BSI-C5') and ISO27001.
    // A mismatch causes MappingLibraryLoader to silently skip ALL mapping pairs
    // (exact-code match in MappingLibraryLoader.php). The full-catalogue command
    // is the registry-bound loader for BSI-C5 since the full/partial split fix.

    /**
     * Returns the DB code for BSI C5:2020 as defined by the registry-bound
     * LoadC52020FullCatalogueCommand (the single `app.framework_loader` for BSI-C5).
     */
    private static function expectedC5FrameworkCode(): string
    {
        $cmd = new \ReflectionClass(LoadC52020FullCatalogueCommand::class);
        $instance = $cmd->newInstanceWithoutConstructor();

        return $instance->getFrameworkCode();
    }

    #[Test]
    public function official_crt_source_framework_matches_c5_db_code(): void
    {
        $expected = self::expectedC5FrameworkCode();
        $actual = self::$data['library']['source_framework'] ?? null;
        self::assertSame(
            $expected,
            $actual,
            sprintf(
                'official-crt source_framework "%s" must match DB code "%s" from LoadC52020FullCatalogueCommand::getFrameworkCode() — '
                . 'a mismatch causes MappingLibraryLoader to silently skip all mapping pairs.',
                (string) $actual,
                $expected,
            ),
        );
    }

    #[Test]
    public function official_crt_target_framework_is_iso27001(): void
    {
        $actual = self::$data['library']['target_framework'] ?? null;
        self::assertSame(
            'ISO27001',
            $actual,
            sprintf(
                'official-crt target_framework "%s" must be "ISO27001" to match the ISO framework DB code.',
                (string) $actual,
            ),
        );
    }

    /**
     * All C5-to-ISO mapping files must use the correct DB framework codes.
     *
     * @return array<string, array{string}>
     */
    public static function allC5ToIsoMappingFiles(): array
    {
        $base = __DIR__ . '/../../../fixtures/library/mappings/';

        return [
            'official-crt'     => [$base . 'bsi-c5-2020_to_iso27001-2022_official-crt_v1.yaml'],
            'heuristic-c5-v1'  => [$base . 'bsi-c5-2020_to_iso27001-2022_v1.0.yaml'],
            'reverse-iso-c5'   => [$base . 'iso27001-2022_to_bsi-c5-2020_v1.0.yaml'],
        ];
    }

    #[Test]
    #[DataProvider('allC5ToIsoMappingFiles')]
    public function c5_iso_mapping_framework_codes_match_db(string $file): void
    {
        $data = Yaml::parseFile($file);
        $lib = $data['library'] ?? [];
        $source = $lib['source_framework'] ?? null;
        $target = $lib['target_framework'] ?? null;
        $c5DbCode = self::expectedC5FrameworkCode();

        // One of source or target must be the C5 DB code, the other must be ISO27001
        $usesC5 = ($source === $c5DbCode || $target === $c5DbCode);
        $usesIso = ($source === 'ISO27001' || $target === 'ISO27001');

        self::assertTrue(
            $usesC5,
            sprintf(
                'File %s: neither source_framework "%s" nor target_framework "%s" matches '
                . 'the C5 DB code "%s". Mismatch silently breaks all C5<->ISO mappings in MappingLibraryLoader.',
                basename($file),
                (string) $source,
                (string) $target,
                $c5DbCode,
            ),
        );

        self::assertTrue(
            $usesIso,
            sprintf(
                'File %s: neither source_framework "%s" nor target_framework "%s" is "ISO27001". '
                . 'Mismatch prevents C5<->ISO cross-framework lookups.',
                basename($file),
                (string) $source,
                (string) $target,
            ),
        );
    }
}
